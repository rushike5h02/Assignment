   // print 25 to 50 using While Loop

public class PrintNo {

	public static void main(String[] args) {
		
		int i=25;                      // initialization
		
		while(i<=50) {                                   // Condition
			System.out.println("Number is=="+i);         // Output
			i++;                                     // Operation
		}
		

	}

}
